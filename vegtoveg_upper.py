#convert all the values in the list veg to upper caseand add them to another list
# create a separate list

veg=['brinjal', 'okra', 'bottle guard', 'kothmir']
veg_new=[]
for each_item in veg:
    x=each_item.upper()
    veg_new.append(x)
print(veg, "is converted into uppercase like this ", veg_new)
