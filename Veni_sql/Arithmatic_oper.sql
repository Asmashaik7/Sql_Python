-- WRITE A QUERY TO GET ADD THE COST+SALES
SELECT PNAME AS PRODUCTS,
COST,
SALES,
SALES+COST AS ADDITION 
FROM PRODUCTS

/*Result:
PRODUCTS	COST	SALES	ADDITION
MOBILES	300	500	800
LAPTOPS	500	600	1100
MOUSES	150	200	350*/

------------------------------------------------------
-- WRITE A QUERY TO GET DIFFERENCE OF SALES-COST
SELECT PNAME,
SALES,
COST,
SALES-COST AS PROFIT 
FROM PRODUCTS

/*Result:
PNAME	SALES	COST	PROFIT
MOBILES	500	300	200
LAPTOPS	600	500	100
MOUSES	200	150	50*/
----------------------------------------------------
-- WRITE A QUERY TO GET MUL OF QUANTITY*COST
SELECT PNAME,
QUANTITY,
COST,
QUANTITY*COST AS AMOUNT 
FROM PRODUCTS

/*Result:
PNAME	QUANTITY	COST	AMOUNT
MOBILES	4	300	1200
LAPTOPS	5	500	2500
MOUSES	2	150	300*/


-- WRITE A QUERY TO GET TOTAL PROFITS FOR EACH PRODUCT
SELECT PNAME,
QUANTITY,
SALES,
COST,(SALES-COST)*QUANTITY AS TOTAL_PROFIT 
FROM PRODUCTS

/*Result:
PNAME	QUANTITY	SALES	COST	TOTAL_PROFIT
MOBILES	4	500	300	800
LAPTOPS	5	600	500	500
MOUSES	2	200	150	100*/


-- WRITE A QUERY TO GET SALES/COST
SELECT PNAME,
SALES,
COST,
SALES/COST AS DIVISION
FROM PRODUCTS

/*Result:
PNAME	SALES	COST	DIVISION
MOBILES	500	300	1
LAPTOPS	600	500	1
MOUSES	200	150	1*/

-- WRITE A QUERY TO GET SALES%COST
SELECT PNAME,
SALES,
COST,
SALES%COST AS MODULE
FROM PRODUCTS

/*PNAME	SALES	COST	MODULE
MOBILES	500	300	200
LAPTOPS	600	500	100
MOUSES	200	150	50 */